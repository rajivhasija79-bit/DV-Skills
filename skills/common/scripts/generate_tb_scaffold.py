# ── Main ───────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="S5 TB Scaffold Generator")
    parser.add_argument("--config", required=True,
                        help="Path to tb_scaffold_data.json")
    parser.add_argument("--phase",
                        choices=["vips", "ral", "env", "all"],
                        default="all",
                        help="Which phase to generate (default: all)")
    args = parser.parse_args()

    cfg_path = Path(args.config)
    if not cfg_path.exists():
        print(f"ERROR: config file not found: {cfg_path}", file=sys.stderr)
        sys.exit(1)

    with cfg_path.open() as f:
        cfg_data = json.load(f)

    proj_root = Path(cfg_data.get("project_root", "."))
    dv_root   = proj_root / "dv"
    vips      = cfg_data.get("unique_vips", [])

    print("=" * 60)
    print(f"  DV TB Scaffold Generator — {cfg_data.get('project_name', 'unknown')}")
    print(f"  Phase  : {args.phase}")
    print(f"  DV Root: {dv_root}")
    print("=" * 60)

    if args.phase in ("vips", "all"):
        print("\n[VIPs]")
        for vip in vips:
            vn = vip["vip_name"]
            print(f"  Generating {vn} ({vip['protocol']})...")
            gen_interface(vip, dv_root)
            gen_seq_item(vip, dv_root)
            gen_cfg(vip, dv_root)
            gen_driver(vip, dv_root)
            gen_monitor(vip, dv_root)
            gen_sequencer(vip, dv_root)
            gen_coverage(vip, dv_root)
            gen_agent(vip, dv_root)
            gen_base_seq(vip, dv_root)
            gen_reset_seq(vip, dv_root)
            gen_pkg(vip, dv_root)

    if args.phase in ("ral", "all"):
        print("\n[RAL]")
        gen_ral(cfg_data, dv_root)

    if args.phase in ("env", "all"):
        print("\n[Environment]")
        gen_env_cfg(cfg_data, dv_root)
        gen_ref_model(cfg_data, dv_root)
        gen_scoreboard(cfg_data, dv_root)
        gen_env(cfg_data, dv_root)
        gen_virtual_seqr(cfg_data, dv_root)
        gen_tb_top(cfg_data, dv_root)
        gen_dut_stub(cfg_data, dv_root)
        gen_env_pkg(cfg_data, dv_root)

    print("\n" + "=" * 60)
    print("  Generation complete.")
    print("  All generated files use safe_write() — existing files NOT overwritten.")
    print("=" * 60)


if __name__ == "__main__":
    main()
