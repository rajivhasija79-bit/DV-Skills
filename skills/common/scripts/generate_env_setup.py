# ─── Main ─────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="S4 DV Environment Setup Generator"
    )
    parser.add_argument("--config",    required=True,
                        help="Path to dv_env_config.json")
    parser.add_argument("--overwrite", action="store_true",
                        help="Overwrite existing files (default: skip)")
    args = parser.parse_args()

    cfg       = load_config(args.config)
    p         = cfg["project_name"]
    proj_root = Path(cfg.get("project_root", f"./{p}")).resolve()
    ow        = args.overwrite

    print(f"\n{'='*62}")
    print(f"  dv-env-setup (S4) — Generating {p} DV Environment")
    print(f"  Project root : {proj_root}")
    print(f"{'='*62}\n")

    # ── Directory scaffold ────────────────────────────────────────────────────
    print("[ Creating directory scaffold ]")
    dirs = [
        proj_root / "rtl",
        proj_root / "dv" / "tb",
        proj_root / "dv" / "env",
        proj_root / "dv" / "seq_lib",
        proj_root / "dv" / "agents",
        proj_root / "dv" / "sim" / "build",
        proj_root / "dv" / "sim" / "run",
        proj_root / "dv" / "sim" / "cov",
        proj_root / "dv" / "scripts",
        proj_root / "dv" / "cov",
    ]
    for d in dirs:
        mkdir(d)
        print(f"  [DIR]   {d}")

    # ── Environment files ─────────────────────────────────────────────────────
    print("\n[ Generating environment files ]")
    write_file(proj_root / "proj.cshrc",  gen_cshrc(cfg, proj_root),  ow)
    write_file(proj_root / "proj.bashrc", gen_bashrc(cfg, proj_root), ow)
    write_file(proj_root / ".env",        gen_dotenv(cfg, proj_root), ow)

    # ── VCS simulation files ──────────────────────────────────────────────────
    print("\n[ Generating VCS simulation files ]")
    sim = proj_root / "dv" / "sim"
    write_file(sim / "synopsys_sim.setup", gen_synopsys_sim_setup(cfg),    ow)
    write_file(sim / "rtl.f",             gen_rtl_f(cfg, proj_root),       ow)
    write_file(sim / "compile.f",         gen_compile_f(cfg, proj_root),   ow)
    write_file(sim / "Makefile",          gen_makefile(cfg, proj_root),     ow)
    write_file(sim / "waves.tcl",         gen_waves_tcl(cfg, proj_root),   ow)
    write_file(sim / "regression.sh",     gen_regression_sh(cfg, proj_root),ow)
    # Make regression.sh executable
    rsh = sim / "regression.sh"
    if rsh.exists():
        rsh.chmod(rsh.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP)

    # ── UVM TB skeleton files ─────────────────────────────────────────────────
    print("\n[ Generating UVM testbench skeleton files ]")
    tb  = proj_root / "dv" / "tb"
    env = proj_root / "dv" / "env"
    seq = proj_root / "dv" / "seq_lib"

    write_file(tb  / f"{p}_tb_top.sv",     gen_tb_top(cfg),    ow)
    write_file(tb  / f"{p}_if.sv",         gen_if(cfg),        ow)
    write_file(env / f"{p}_env_pkg.sv",    gen_env_pkg(cfg),   ow)
    write_file(env / f"{p}_env_cfg.sv",    gen_env_cfg(cfg),   ow)
    write_file(env / f"{p}_scoreboard.sv", gen_scoreboard(cfg),ow)
    write_file(env / f"{p}_coverage.sv",   gen_coverage(cfg),  ow)
    write_file(env / f"{p}_env.sv",        gen_env(cfg),       ow)
    write_file(seq / f"{p}_base_seq.sv",   gen_base_seq(cfg),  ow)
    write_file(seq / f"{p}_base_test.sv",  gen_base_test(cfg), ow)
    write_file(seq / f"{p}_sanity_test.sv",gen_sanity_test(cfg),ow)

    # ── Output JSON for S5+ ───────────────────────────────────────────────────
    print("\n[ Writing dv_env_data.json for downstream skills ]")
    out_json = f"/tmp/{p}_env_data_output.json"
    Path(out_json).write_text(gen_env_data_json(cfg, proj_root))
    print(f"  [GEN]   {out_json}")

    # ── Terminal summary ──────────────────────────────────────────────────────
    print(f"\n{'='*62}")
    print(f"  dv-env-setup (S4) — Complete")
    print(f"  Project   : {p}")
    print(f"  Root      : {proj_root}")
    print(f"{'─'*62}")
    print(f"  Generated :")
    print(f"    Directories      : {len(dirs)}")
    print(f"    Env files        : 3  (proj.cshrc, proj.bashrc, .env)")
    print(f"    VCS sim files    : 6  (Makefile, compile.f, rtl.f,")
    print(f"                          synopsys_sim.setup, waves.tcl,")
    print(f"                          regression.sh)")
    print(f"    UVM TB skeletons : 10")
    print(f"{'─'*62}")
    print(f"  Quick start:")
    print(f"    source {proj_root}/proj.cshrc")
    print(f"    cd {proj_root}/dv/sim")
    print(f"    make compile        # after adding RTL files to rtl.f")
    print(f"    make sim TEST=sanity SEED=1 WAVES=1")
    print(f"    make verdi TEST=sanity SEED=1")
    print(f"    make regress")
    print(f"    make cov_merge && make cov_report")
    print(f"{'─'*62}")
    print(f"  Next step: run /dv-tb-scaffold (S5) to generate agents")
    print(f"{'='*62}\n")


if __name__ == "__main__":
    main()
