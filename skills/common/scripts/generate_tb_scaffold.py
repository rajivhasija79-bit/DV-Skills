# ── Helpers ────────────────────────────────────────────────────────────────────

def safe_write(path: Path, content: str) -> bool:
    """Write file only if it does not already exist. Returns True if written."""
    if path.exists():
        print(f"  SKIP (exists): {path}")
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    print(f"  WRITE: {path}")
    return True


def vip_class(vip_name: str, suffix: str, params: dict) -> str:
    """Return parameterized class name string."""
    data_w = params.get("DATA_WIDTH", 32)
    addr_w = params.get("ADDR_WIDTH", 32)
    return f"{vip_name}_{suffix} #(.DATA_WIDTH({data_w}), .ADDR_WIDTH({addr_w}))"


def param_decl(params: dict) -> str:
    """Return parameter declaration block."""
    lines = []
    for k, v in params.items():
        lines.append(f"  parameter int {k} = {v}")
    return ",\n".join(lines)


def header(vip_name: str, file_type: str, protocol: str, params: dict) -> str:
    data_w = params.get("DATA_WIDTH", 32)
    addr_w = params.get("ADDR_WIDTH", 32)
    return (f"// {vip_name.upper()} {file_type} — generated by dv-tb-scaffold (S5)\n"
            f"// Protocol: {protocol}  DATA_WIDTH={data_w}  ADDR_WIDTH={addr_w}\n"
            f"// Generated: {date.today()}  DO NOT edit generated sections.\n\n")


