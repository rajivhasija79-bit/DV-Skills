def gen_rtl_f(cfg, proj_root):
    p         = cfg["project_name"]
    pr        = str(proj_root)
    rtl_files = cfg.get("rtl_files", [])
    if rtl_files:
        file_lines = "\n".join(f"{pr}/rtl/{f}" for f in rtl_files)
    else:
        file_lines = "// TODO: Add RTL source files in compilation order (packages first)\n// ${RTL_ROOT}/<module>.sv"
    return (
        f"// rtl.f — RTL Compilation File List\n"
        f"// Generated by dv-env-setup (S4)   Date: {TODAY}\n"
        "// Passed to vlogan for the RTL compile pass (make compile_rtl)\n\n"
        "//----------------------------------------------------------------------\n"
        "// RTL include directories\n"
        "//----------------------------------------------------------------------\n"
        f"+incdir+{pr}/rtl\n\n"
        "//----------------------------------------------------------------------\n"
        "// RTL source files — add in compilation order (packages before modules)\n"
        "//----------------------------------------------------------------------\n"
        f"{file_lines}\n"
    )


