#!/usr/bin/env python3
"""
generate_env_setup.py — S4 DV Environment Setup Generator

Reads a dv_env_config.json and generates the complete DV project environment:
  - Directory scaffold
  - Environment files: proj.cshrc, proj.bashrc, .env
  - VCS simulation files: Makefile, synopsys_sim.setup, compile.f, rtl.f,
                          waves.tcl, regression.sh
  - UVM TB skeleton files (10 files)
  - dv_env_data.json output for S5+

Usage:
    python3 generate_env_setup.py --config /tmp/<proj>_env_config.json
    python3 generate_env_setup.py --config /tmp/<proj>_env_config.json --overwrite
"""

